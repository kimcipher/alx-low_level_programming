#include "3-calc.h"
#include <stddef.h>
#include<stdio.h>
#include<stdlib.h>

/**
 * op_add - add function
 * @x: input one
 * @y: input two
 * Return: 0
 */


int op_add(int x, int y)
{

	return (x + y);
}
/**
 * op_sub - subtract function
 * @x: input one
 * @y: input two
 * Return: 0
 */
int op_sub(int x, int y)
{

	return (x - y);
}
/**
 * op_mul - multiplication function
 * @x: input one
 * @y: input two
 * Return: 0
 */
int op_mul(int x, int y)
{

	return (x * y);
}
/**
 * op_div - division function
 * @x: input one
 * @y: input two
 * Return: 0
 */
int op_div(int x, int y)
{

	return (x / y);
}
/**
 * op_mod - modulous function
 * @x: input one
 * @y: input two
 * Return: 0
 */
int op_mod(int x, int y)
{

	return (x % y);
}
